const urlParams = new URLSearchParams(window.location.search);
const quizKey = urlParams.get("quiz") || "mern-advanced";
const quizData = questionBank[quizKey];

// Elements
const quizTitle = document.getElementById("quiz-title");
const questionEl = document.getElementById("question");
const optionsDiv = document.getElementById("options");
const resultDiv = document.getElementById("result");
const nextBtn = document.getElementById("nextBtn");

let current = 0;
let score = 0;

quizTitle.innerText = quizData.title;

// Load question
function loadQuestion() {
  resultDiv.innerHTML = "";
  nextBtn.style.display = "inline-block";

  if (current >= quizData.questions.length) {
    showScore();
    return;
  }

  const q = quizData.questions[current];
  questionEl.innerText = q.question;
  optionsDiv.innerHTML = "";

  q.options.forEach((opt, i) => {
    const btn = document.createElement("button");
    btn.innerText = opt;
    btn.className = "option-btn";
    btn.onclick = () => selectAnswer(i);
    optionsDiv.appendChild(btn);
  });

   history.pushState({ current }, `Question ${current + 1}`, `#q${current}`);
}

// Option selected
function selectAnswer(i) {
  // Increment score if correct
  if (i === quizData.questions[current].answer) score++;
  current++;
  loadQuestion();

}


// Show score
function showScore() {
  const total = quizData.questions.length;
  const percent = Math.round((score / total) * 100);

  // Performance message
  let performance = "";
  if (percent === 100) {
    performance = "🌟 Excellent! Perfect Score!";
  } else if (percent >= 75) {
    performance = "👏 Great Job! You did really well.";
  } else if (percent >= 50) {
    performance = "🙂 Good Effort! Keep practicing.";
  } else {
    performance = "😢 Needs Improvement. Try again!";
  }

  // Fullscreen overwrite with boxes
  document.body.innerHTML = `
    <div class="score-page">
      <h1>🎉 Quiz Completed!</h1>
      <p class="performance">${performance}</p>

      <div class="score-grid">
        <div class="score-box correct">
          <h3>✅ Correct</h3>
          <p>${score}</p>
        </div>
        <div class="score-box wrong">
          <h3>❌ Wrong</h3>
          <p>${total - score}</p>
        </div>
        <div class="score-box total">
          <h3>📊 Total</h3>
          <p>${total}</p>
        </div>
        <div class="score-box percent">
          <h3>💯 Percentage</h3>
          <p>${percent}%</p>
        </div>
      </div>

      <div class="progress-bar">
        <div class="progress-fill" style="width: ${percent}%"></div>
      </div>

      <button onclick="location.reload()" class="restart-btn">🔄 Restart Quiz</button>
    </div>
  `;
}





